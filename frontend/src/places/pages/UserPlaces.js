const UserPlaces = () => {
  const userId = useParams().userId;
  //     Now use params is simply executed as a function and it gives us access to the parameters as it's

// called,

// so the dynamic segments React router is aware of. So dynamic segments are these things you add in code with a

// colon in your route configuration.

// So in this case we can get the user ID and use params will return an object which has your dynamic

// segments you set up in the route config as properties,

// so for the user places component there will be an object with a user ID property which then in turn

// holds the concrete value that's entered in the URL as a value. So here I can access user ID and

// store that in let's say the user ID constant
  const loadedPlaces = DUMMY_PLACES.filter(place => place.creator === userId);
  return <PlaceList items={loadedPlaces} />;
};

export default UserPlaces;
